/**
 * #4 :: Export JS function "isGreaterThan" to check whether one number is greater than a given number
 * input: number (value)
 * input: number (threshold)
 * output: boolean
 */

/**
 * 
 * @param {number} number 
 * @param {number} threshold 
 * @returns {boolean} Returns true if the number is greater than the threshold
 */
function isGreaterThan(number, threshold) {
  return number > threshold;
};
export default isGreaterThan;
